/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) {
 *         val = x;
 *         next = null;
 *     }
 * }
 */
public class Solution {
    public ListNode getIntersectionNode(ListNode headA, ListNode headB) {
        ListNode tempA = headA, tempB = headB;
        while (tempA != tempB) {
            if (tempA != null) { // 为什么使用 tempA != null ，而不使用 tempA.next != null？
                // 使用 tempA != null 相较于 tempA.next != null 在循环上多走了两步，但是其可以将 null 带入到循环中去判断
                // 而 tempA.next != null 将不会将 null 节点带入到循环中去判断，将会导致没有相交接入点的时候，一致死循环下去，其
                //      在程序中也解决，无非是加响应的标志位，这样会导致代码的逻辑更加复杂，相关的系统资源开销也会更高。
                tempA = tempA.next;
            } else {
                tempA = headB;
            }
            if (tempB != null) {
                tempB = tempB.next;
            } else {
                tempB = headA;
            }
        }
        return tempA;
    }
}
